SELECT a.flavor
FROM FIRST_HALF a
    ,ICECREAM_INFO b
WHERE a.flavor = b.flavor
  AND a.total_order > 3000
  AND b.ingredient_type = 'fruit_based'
ORDER BY a.total_order DESC;


